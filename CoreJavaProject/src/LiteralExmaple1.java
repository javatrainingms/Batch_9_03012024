
public class LiteralExmaple1 {

	public static void main(String[] args) {
		int i=10;   //normal decimal
		System.out.println("i value>>"+i);
		
		int i1='a';  //character
		System.out.println("i1 value<>>>"+i1);
		
		int i2=010;  //octa decimal
		System.out.println("i2 value>>>"+i2);
		
		int i3=0x10;  // hexa decimal
		System.out.println("i3 value>>>"+i3);
	}
}
