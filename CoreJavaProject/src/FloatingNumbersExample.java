
public class FloatingNumbersExample {
	public static void main(String[] args) {
		float f=100;  //input ==>normal num==>convert into float assign to float
		System.out.println("f value>>>"+f);
		
		float f1=100.0F; //while taking input floating numbers
		System.out.println("f1 value>>>>>>>"+f1);
		
		double d=100;
		System.out.println("d value>>"+d);
		
		double d1=100.0;
		System.out.println("d1 value>>>"+d1);
		
	}

}
